#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *s3;

	s3 = s1;
	while (*s3)
		s3++;
	while (*s2 && n--)
		*(s3++) = *(s2++);
	*s3 = '\0';
	return (s1);
}
